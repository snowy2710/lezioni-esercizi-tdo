#FASI DI UN PROCESSO DI ANALISI DI DATI

#import: i dati devono essere importati in R

#tidy: riordinare i dati per dare loro una forma e una semantica utile per l'analisi

#transform: il dataset, ben organizzato, può essere modificato ai fini della specifica analisi

#visualize: visualizzare i risultati di un'analiosi è fondamentare. esistono modi e forme differenti per visualizzare dei dati, con pro e contro

#model: un modello è una rappresentazione semplificata della realtà e serve per comprendere e rispondere a domande

#communicate: con la comunicazione si presentano i risultati dell'analisi, della visualizzazione e della modellazione.






































































































































































































































